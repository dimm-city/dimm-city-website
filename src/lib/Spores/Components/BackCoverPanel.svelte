        <h1>Dimm City Spore</h1>
        <p>
            A Dimm City Spore is a rules light (Lasers & Feelings homebrew) introduction to the Dimm City Campaign
            Setting/WEB3 Character Evolution for use with the rules heavy Quest RPG System.
        </p>
        <p>
            In a Dimm City Spore, Dreamers (players) take on the role of a sporos (an animal/hybrid humanoid) hero doing
            good deeds in a bad place using magic, technology, combat, and their own wits to succeed. Dream Masters (the
            judge and storyteller) provide environmental descriptions, atmosphere, challenges, and opponents along with
            the plot to drive the Dream (shared story) forward. The Dream Master is not an enemy of the Dreamers, but a
            guide into Dimm City and all the action, adventure, and secrets it holds. Dreamers and the DM work together
            to create a story that is both exciting and unforgettable.
        </p>

        <h1>What is Dimm City?</h1>
        <p>
            Dimm City is a collection of interconnected pocket dimensions that exist floating in the Ether. Dreamers
            that connect to the ether (through learning to play the game) come into possession of a sporos and enter
            Dimm City through that being.
        </p>
        <p>
            A sporos may be brand new to the city or previously established and newly connected to the Dreamer. Once
            inside, the Dreamer must decide their path in the city. Will they be sucked into servitude of the powers
            that be or join with other sporos in their quest to remain free?
        </p>
