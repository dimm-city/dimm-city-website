        <h1>Rolling the Dice</h1>
        <p>
            When you want to do something outside of the usual, your DM may call for you to roll a 1d6 (six sided die)
            to determine your success or failure. Roll +1d if you’re prepared and +1d if you’re skilled. If you or a
            friend want to jump in and help out, you can give them a +1d by narrating how you are assisting them in
            their attempt. (The DM determines the number of dice rolled and the difficulty of the situation you face.)
            Roll your dice and compare each die result to your number you chose in step 5 of character creation.
        </p>

        <p>
            <strong>Over:</strong> If you are DREAMING (using magic or fluid people skills), you want to roll above that
            number
        </p>
        <p>
            <strong>Under:</strong> If you are AWAKE (using technology or physical skills), you want to roll below that number
        </p>
        <p>
            <strong>0:</strong> If none of your dice succeed, the attempt goes sideways, The DM will tell you the details
        </p>
        <p>
            <strong>1:</strong> If one die succeeds, you make it by the skin of your teeth. There is a cost though and the
            GM will inform you of the price you pay
        </p>
        <p>
            <strong>2:</strong> If two dice succeed, you pulled it off! SCHWING!!!
        </p>
        <p>
            <strong>3:</strong> If three dice succeed, you perform flawlessly and receive an extra boon the DM will tell
            you about+
        </p>
        <p>
            <strong>!!</strong> If you roll your number exactly on all dice, you become LUCID. While LUCID, you see your
            current situation with additional insight. Ask the DM a question and they’ll answer you honestly.
        </p>
        <p>
            After learning this information, you may optionally change the action you rolled for originally and roll
            again to do something completely different.
        </p>
    </div>
